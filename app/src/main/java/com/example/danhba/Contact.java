package com.example.danhba;

public class Contact {
    private int id;
    private String name;
    // Thêm các thuộc tính khác của liên lạc nếu cần

    // Constructor
    public Contact() {
    }

    // Getter và setter cho id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter và setter cho name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // Thêm getter và setter cho các thuộc tính khác nếu cần
}
